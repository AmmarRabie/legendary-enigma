public class NewFeed {

    private String contentLink;
    private String creatorEmail;
    private String creatorName;
    private String contentLabel;
    private String creationDate;
    private String creatorDep;
    private String creatorFac;
    private String creatorUni;

    public String getContentLink() {
        return contentLink;
    }

    public void setContentLink(String contentLink) {
        this.contentLink = contentLink;
    }

    public String getCreatorEmail() {
        return creatorEmail;
    }

    public void setCreatorEmail(String creatorEmail) {
        this.creatorEmail = creatorEmail;
    }

    public String getCreatorName() {
        return creatorName;
    }

    public void setCreatorName(String creatorName) {
        this.creatorName = creatorName;
    }

    public String getContentLabel() {
        return contentLabel;
    }

    public void setContentLabel(String contentLabel) {
        this.contentLabel = contentLabel;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }

    public String getCreatorDep() {
        return creatorDep;
    }

    public void setCreatorDep(String creatorDep) {
        this.creatorDep = creatorDep;
    }

    public String getCreatorFac() {
        return creatorFac;
    }

    public void setCreatorFac(String creatorFac) {
        this.creatorFac = creatorFac;
    }

    public String getCreatorUni() {
        return creatorUni;
    }

    public void setCreatorUni(String creatorUni) {
        this.creatorUni = creatorUni;
    }
}
